package interview;

public class StringCompare {

	private String first;
	
	private String second;
	
	public StringCompare(String first, String second) {
		this.first = first;
		this.second = second;
	}
	
	public String getFirst() {
		return this.first;
	}
	
	public String getSecond() {
		return this.second;
	}
	
}
